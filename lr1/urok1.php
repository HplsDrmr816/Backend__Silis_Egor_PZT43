<?php
$name = "Егор"; // переменная для подстановки
?>
<!DOCTYPE html>
<html lang="ru">
<head><meta charset="UTF-8"><title>Урок 1 — echo</title></head>
<body>
    <h2>1. Вывод текста через echo</h2>
    <?php
    // Вывод одной строки
    echo "Привет, мир!<br>";

    // Вывод нескольких строк через запятую
    echo "Это мой первый урок по PHP.", "<br>";

    // Вывод HTML-тегов
    echo "<h3>Заголовок</h3>";
    echo "<p>Это абзац текста, выведенный через PHP.</p>";
    ?>

    <hr>
    <h2>2. Вывод переменных</h2>
    <?php
    // В двойных кавычках значение переменной подставляется автоматически
    echo "Привет, меня зовут $name!<br>";

    // В одинарных кавычках переменная выводится как текст
    echo 'Привет, меня зовут $name!<br>';

    // Экранирование кавычек обратным слэшем
    echo "Он сказал: \"PHP — это мощно!\"<br>";
    ?>
